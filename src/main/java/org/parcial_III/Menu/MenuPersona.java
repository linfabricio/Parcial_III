    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        int confirmar = JOptionPane.showConfirmDialog(null, "Deseas agregar persona?");
        if (confirmar == JOptionPane.YES_OPTION) {
            persona = new PersonaModels();
            persona.Id = Integer.parseInt(jTextField1.getText());
            persona.IdCiudad = Integer.valueOf(jTextField2.getText());
            persona.Nombre = jTextField3.getText();
            persona.Apellido = jTextField4.getText();
            persona.TipoDocumento = jTextField5.getText();
            persona.NroDocumento = jTextField6.getText();
            persona.Direccion = jTextField7.getText();
            persona.Celular = jTextField8.getText();
            persona.Email = jTextField9.getText();
            persona.Estado = jTextField10.getText();
            personaService.registrarPersona(persona);
            String mensaje = "La persona " + persona.IdCiudad + " fue registrada correctamente!!!";

            JOptionPane.showMessageDialog(null, mensaje, "Éxito", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

